public class exercise_1 {
    public static void main(String[] args) {
        int i = 5 + 2; // 7
        int n = i * 3; // 7 * 3 --> 21
        int m = n / 4; // 21 / 4 --> 5.25 --> 5
        int p = m - i; // 5 - 7 --> -2
        int q = -p; // -(-2) --> 2

        System.out.println(i);
        System.out.println(n);
        System.out.println(m);
        System.out.println(p);
        System.out.println(q);
    }
}
