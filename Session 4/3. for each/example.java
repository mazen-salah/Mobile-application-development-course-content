public class example {
    public static void main(String[] args) {
        String firstName = "Alice";
        String lastName = "Bob";

        System.out.println(firstName.length());
        System.out.println(lastName.length());
        System.out.println(firstName + lastName);
        System.out.println(firstName.concat(lastName));
        System.out.println(firstName.substring(1, 3));
        System.out.println(lastName.substring(1, 3));
        System.out.println(firstName.equals(lastName));
        System.out.println(firstName.charAt(1));
        System.out.println(lastName.charAt(1));
        System.out.println(firstName.isEmpty());
        System.out.println(lastName.isEmpty());
        System.out.println(firstName.toUpperCase());
        System.out.println(lastName.toUpperCase());
        System.out.println(firstName.toLowerCase());
        System.out.println(lastName.toLowerCase());
    } 
}
